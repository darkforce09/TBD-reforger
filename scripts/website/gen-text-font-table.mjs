#!/usr/bin/env node
// T-152.13 — generate crates/map-engine-render/src/text_font_table.rs from a Spleen 16x32 BDF.
//
// Source font: Spleen 16x32 (BSD-2-Clause, Copyright (c) 2018-2026, Frederic Cambus)
//   release: https://github.com/fcambus/spleen/releases/download/2.2.0/spleen-2.2.0.tar.gz
//   sha256(spleen-2.2.0.tar.gz): ec42925c6b56d2138c862b2f97147c872e472f674bf03423417d827a08d69a89
//   BDF inside: spleen-2.2.0/spleen-16x32.bdf
//
// Regenerate:
//   curl -sLO https://github.com/fcambus/spleen/releases/download/2.2.0/spleen-2.2.0.tar.gz
//   tar xzf spleen-2.2.0.tar.gz spleen-2.2.0/spleen-16x32.bdf
//   node scripts/website/gen-text-font-table.mjs spleen-2.2.0/spleen-16x32.bdf \
//     > crates/map-engine-render/src/text_font_table.rs
//
// Output: 95 printable-ASCII glyphs (U+0020..U+007E) as [[u16; 32]; 96] — one u16 per pixel
// row, bit 15 = leftmost pixel. Index 95 (tofu) is zeroed here; the atlas baker draws the
// tofu box procedurally. A few ASCII-art proofs go to stderr for eyeball sanity.

import { readFileSync } from 'node:fs';

const GLYPH_W = 16;
const GLYPH_H = 32;
const FIRST = 32;
const LAST = 126;

const bdfPath = process.argv[2];
if (!bdfPath) {
  console.error('usage: gen-text-font-table.mjs <spleen-16x32.bdf>');
  process.exit(1);
}
const bdf = readFileSync(bdfPath, 'utf8');

// Parse BDF: STARTCHAR … ENCODING n … BBX w h xoff yoff … BITMAP <h hex rows> ENDCHAR.
// Spleen is a strict cell font (DWIDTH 16 0, BBX 16 32 0 -6 everywhere) so rows map 1:1
// onto the 16x32 cell; assert that instead of implementing general BBX offset composition.
const glyphs = new Map();
const charBlocks = bdf.split(/^STARTCHAR /m).slice(1);
for (const block of charBlocks) {
  const enc = Number(/^ENCODING (-?\d+)$/m.exec(block)?.[1]);
  if (!(enc >= FIRST && enc <= LAST)) continue;
  const bbx = /^BBX (-?\d+) (-?\d+) (-?\d+) (-?\d+)$/m.exec(block);
  if (!bbx) throw new Error(`U+${enc.toString(16)}: no BBX`);
  const [w, h, xo, yo] = bbx.slice(1).map(Number);
  if (w !== GLYPH_W || h !== GLYPH_H || xo !== 0) {
    throw new Error(`U+${enc.toString(16)}: BBX ${w} ${h} ${xo} ${yo} — not a full 16x32 cell`);
  }
  const bitmapAt = block.indexOf('BITMAP');
  const rows = block
    .slice(bitmapAt)
    .split('\n')
    .slice(1, 1 + GLYPH_H)
    .map((line) => parseInt(line.trim(), 16));
  if (rows.length !== GLYPH_H || rows.some((r) => Number.isNaN(r))) {
    throw new Error(`U+${enc.toString(16)}: bad bitmap (${rows.length} rows)`);
  }
  glyphs.set(enc, rows);
}

for (let c = FIRST; c <= LAST; c++) {
  if (!glyphs.has(c)) throw new Error(`missing glyph U+${c.toString(16)} '${String.fromCharCode(c)}'`);
}

// Eyeball proof on stderr.
for (const ch of ['7', 'a', 'A', '-']) {
  const rows = glyphs.get(ch.charCodeAt(0));
  console.error(`── '${ch}' ──`);
  for (const r of rows) {
    console.error(
      [...Array(GLYPH_W)].map((_, x) => ((r >> (15 - x)) & 1 ? '█' : '·')).join('')
    );
  }
}

const out = [];
out.push('//! GENERATED by `scripts/website/gen-text-font-table.mjs` — DO NOT EDIT BY HAND.');
out.push('//!');
out.push('//! Glyph raster data extracted from **Spleen 16x32 v2.2.0**');
out.push('//! Copyright (c) 2018-2026, Frederic Cambus — BSD-2-Clause (SPDX: BSD-2-Clause).');
out.push('//! <https://github.com/fcambus/spleen> · release tarball sha256');
out.push('//! `ec42925c6b56d2138c862b2f97147c872e472f674bf03423417d827a08d69a89`.');
out.push('//!');
out.push('//! Redistribution notice (BSD-2-Clause): redistributions of source code must retain');
out.push('//! the above copyright notice; see the upstream `LICENSE` file for the full text.');
out.push('');
out.push('/// Glyph ink width in pixels (half the 32 px atlas cell).');
out.push(`pub const FONT_GLYPH_W: u32 = ${GLYPH_W};`);
out.push('/// Glyph height in pixels (fills the 32 px atlas cell).');
out.push(`pub const FONT_GLYPH_H: u32 = ${GLYPH_H};`);
out.push('');
out.push('/// One u16 per pixel row, bit 15 = leftmost pixel. Index = ASCII − 32 for');
out.push('/// U+0020..=U+007E; index 95 is all-zero (the baker paints the tofu box there).');
out.push('#[rustfmt::skip]');
out.push(`pub const FONT_16X32: [[u16; ${GLYPH_H}]; 96] = [`);
for (let c = FIRST; c <= LAST + 1; c++) {
  const rows = c <= LAST ? glyphs.get(c) : new Array(GLYPH_H).fill(0);
  const label = c <= LAST ? `'${c === 39 ? "\\'" : c === 92 ? '\\\\' : String.fromCharCode(c)}'` : 'tofu (baker-drawn)';
  const hex = rows.map((r) => `0x${r.toString(16).padStart(4, '0')}`).join(', ');
  out.push(`    [${hex}], // ${label}`);
}
out.push('];');
console.log(out.join('\n'));
