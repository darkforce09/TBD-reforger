def cmd_mark_ready(args: argparse.Namespace) -> None:
    registry = load_registry()
    t = ticket_by_id(registry, args.id)
    if not t:
        sys.stderr.write(f"Unknown ticket: {args.id}\n")
        sys.exit(1)
    if args.spec:
        t["spec"] = args.spec
    spec = (t.get("spec") or "").strip()
    if not spec:
        sys.stderr.write(f"Ticket {args.id} needs a spec path\n")
        sys.exit(1)
    root = repo_root()
    if not (root / spec).is_file():
        sys.stderr.write(f"Spec file not found: {root / spec}\n")
