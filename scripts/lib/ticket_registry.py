def generate_ticket_lead_md(registry: dict[str, Any] | None = None) -> str:
    registry = registry or load_registry()
    tickets = registry.get("tickets", [])
    lines = [AUTO_HEADER, "# Ticket Lead Dashboard", ""]
    for label, statuses in (
        ("Running / Review", ("running", "review", "active")),
        ("Ready", ("ready",)),
        ("Next queued (top 10)", ("queued",)),
    ):
        subset = [t for t in tickets if t.get("status") in statuses]
        subset.sort(key=_ticket_sort_key)
        if label.startswith("Next"):
            subset = subset[:10]
        lines.append(f"## {label}")
        lines.append("")
        for t in subset:
            lines.append(
                f"- **{t['id']}** ({t.get('order', '—')}) — {t.get('title', '')} "
                f"[{t.get('status')}] — {t.get('summary', '')}"
            )
        lines.append("")
    lines.append("## Dependency graph (scoped)")
    lines.append("")
    lines.append("```mermaid")
    lines.append("flowchart LR")
    graph_ids: set[str] = set()
    for t in tickets:
        if t.get("status") in ("running", "review", "ready"):
            graph_ids.add(t["id"])
    queued = sorted(
        [t for t in tickets if t.get("status") == "queued" and t.get("order")],
        key=_ticket_sort_key,
    )[:5]
    for t in queued:
        graph_ids.add(t["id"])
    for t in tickets:
        if t.get("status") == "shipped":
            for other in tickets:
                if other["id"] in graph_ids and t["id"] in (other.get("depends_on") or []):
                    graph_ids.add(t["id"])
    for t in tickets:
        if t["id"] not in graph_ids:
            continue
        for dep in t.get("depends_on") or []:
            if dep in graph_ids:
                dep_n = dep.replace("-", "")
                tid_n = t["id"].replace("-", "")
                lines.append(f"  {dep_n}[{dep}] --> {tid_n}[{t['id']}]")
    lines.append("```")
    lines.append("")
    return "\n".join(lines)


