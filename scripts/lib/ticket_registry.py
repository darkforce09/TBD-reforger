def slice_spec(t: dict[str, Any]) -> str:
    active = t.get("active_slice")
    plan = t.get("slice_plan") or {}
    if active and active in plan and plan[active].get("spec"):
        return str(plan[active]["spec"])
    return str(t.get("spec", ""))


def shipped_slices(t: dict[str, Any]) -> list[str]:
    plan = t.get("slice_plan") or {}
    return [sid for sid, row in plan.items() if row.get("status") == "shipped"]


def slice_executor(t: dict[str, Any]) -> str:
    active = t.get("active_slice")
    plan = t.get("slice_plan") or {}
    if active and active in plan:
        return plan[active].get("executor", t.get("executor", "claude-code"))
    return t.get("executor", "claude-code")


def slice_targets(t: dict[str, Any]) -> list[str]:
    active = t.get("active_slice")
    plan = t.get("slice_plan") or {}
    if active and active in plan:
        return list(plan[active].get("targets") or t.get("targets") or ["website"])
    return list(t.get("targets") or ["website"])


def generate_ticket_brainstorm_md(registry: dict[str, Any] | None = None) -> str:
    registry = registry or load_registry()
    lines = [AUTO_HEADER, "# Ticket Brainstorm", "", "`idea` + `deferred` only.", ""]
    by_program: dict[str, list[dict[str, Any]]] = {}
    for t in registry.get("tickets", []):
        if t.get("status") not in ("idea", "deferred"):
            continue
        by_program.setdefault(t.get("program", "platform"), []).append(t)
    for program in sorted(by_program):
        lines.append(f"## {program}")
        lines.append("")
        for t in sorted(by_program[program], key=_ticket_sort_key):
            surfaces = ", ".join(t.get("surfaces") or [])
            lines.append(
                f"- **{t['id']}** ({t.get('status')}) — {t.get('title', '')} "
                f"[{surfaces}] — {t.get('summary', '')}"
            )
        lines.append("")
    return "\n".join(lines)


