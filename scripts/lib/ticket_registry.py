def sync_gap_analysis_ticket_column(registry: dict[str, Any] | None = None) -> None:
    test_gap_analysis_round_trip()
    registry = registry or load_registry()
    path = gap_analysis_path()
    original = path.read_text(encoding="utf-8")
    doc = parse_gap_analysis_tables(original)
    for table in doc.tables:
        for row in table.rows:
            if len(row) >= 5:
                row[3] = _lookup_ticket_for_gap(registry, row[0], row[1], row[4])
    updated = write_gap_analysis_tables(doc, ticket_column=True)
    path.write_text(updated, encoding="utf-8")


def inject_marker_block(path: Path, start: str, end: str, inner: str) -> None:
    text = path.read_text(encoding="utf-8")
    if start not in text or end not in text:
        raise RuntimeError(f"Missing markers in {path}: {start} / {end}")
    before, rest = text.split(start, 1)
    _, after = rest.split(end, 1)
    new_text = before + start + "\n" + inner.rstrip() + "\n" + end + after
    path.write_text(new_text, encoding="utf-8")


def inject_status_block(registry: dict[str, Any] | None = None) -> None:
    registry = registry or load_registry()
    tickets = registry.get("tickets", [])
    shipped = [t for t in tickets if t.get("status") == "shipped"]
    shipped.sort(key=lambda t: t.get("order", 9999), reverse=True)
    latest = shipped[0]["id"] if shipped else "T-066"
    slice_row = next((t for t in tickets if t.get("active_slice")), None)
    lines = [f"**Latest shipped:** **{latest}**", ""]
    if slice_row:
        slice_id = slice_row.get("active_slice", "")
        slice_read = slice_spec(slice_row)
        lines.append(
            f"**ACTIVE NOW:** **{slice_row['id']}** — {slice_id} "
            f"({slice_row.get('title', '')}). Slice spec: `{slice_read}`."
        )
    else:
        ready = sorted(
            [t for t in tickets if t.get("status") == "ready"],
            key=lambda t: t.get("order", 9999),
        )
        if ready:
            r = ready[0]
            lines.append(f"**ACTIVE NOW:** **{r['id']}** — {r.get('title', '')}")
    lines.append("")
    lines.append("**Next (by order):**")
    queued = [
        t for t in tickets if t.get("status") in ("queued", "ready") and t.get("order")
    ]
    queued.sort(key=lambda t: t.get("order", 9999))
    for t in queued[:10]:
        lines.append(f"- **{t['id']}** — {t.get('title', '')} (`{t.get('status')}`)")
    inject_marker_block(
        repo_root() / "CLAUDE.md", STATUS_MARKER_START, STATUS_MARKER_END, "\n".join(lines)
    )


